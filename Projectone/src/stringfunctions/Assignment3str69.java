package stringfunctions;

public class Assignment3str69 
{

	public static void main(String[] args) 
	{
		String s1="Mom";
		String s2="Dad";
		 
		for(int i=0;i<=s1.length()-1;i++)
		{
			System.out.print(s1.charAt(i));
			System.out.print(s2.charAt(i));

		}
	}

}
